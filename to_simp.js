function toSimplified(text) {
    if (!text || typeof text !== 'string') return text || '';
    let result = text;
    for (const [trad, simp] of Object.entries(TRAD_TO_SIMP_MAP)) {
        if (result.includes(trad)) {
            result = result.split(trad).join(simp);
        }
    }
    return result;
}

